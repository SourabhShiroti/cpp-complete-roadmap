#include<iostream>
using namespace std;
int main()
{
    int a,b;
    cout<<"Enter the first number :";
    cin>>a;
    cout<<"Enter the second number :";
    cin>>b;
    cout<<"addition is :"<<a+b<<endl;
    cout<<"Difference is :"<<a-b<<endl;
    cout<<"Multiplication is :"<<a*b<<endl;
    cout<<"division is :"<<a/b<<endl;
    cout<<"Reminder is :"<<a%b<<endl;
    return 0;
}