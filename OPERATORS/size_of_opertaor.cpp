#include<iostream>
using namespace std;
int main()
{
    int x;
    char y;
    float z;
    double d;
    bool b;
    cout<<"Size of integer datatype :"<<sizeof(x)<<endl;
    cout<<"Size of character datatype :"<<sizeof(y)<<endl;
    cout<<"Size of float datatype :"<<sizeof(z)<<endl;
    cout<<"Size of double datatype :"<<sizeof(d)<<endl;
    cout<<"Size of bool datatype :"<<sizeof(b)<<endl;
    return 0;
}